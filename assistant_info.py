def draw_matrix(m):
    array = m.get_array()
    for y in range(m.get_dy()):
        for x in range(m.get_dx()):
            if array[y][x] == 0:
                print("□ ", end='')
            elif 1 <= array[y][x] <= 7:
                print("■ ", end='')
            elif array[y][x] < 0:
                print("ㅇ ", end='')
            else:
                print("XX", end='')
        print()

